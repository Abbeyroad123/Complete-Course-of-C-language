#include <stdio.h>

int main()
{	
	int a;
	printf("Now I am going to show you use of shift left and shift right operators.\n");
	printf("What no do you want me to perform these operations on?\n");
	scanf("%d", &a);
	
	printf("Now I am going to show you use of shift left operator.\n");	
	printf("a<<2 = %d\n", a<<2);
	
	printf("Now I am going to show you use of shift right operator.\n");	
	printf("a>>2 = %d\n", a>>2);
	
return 0;	
}
